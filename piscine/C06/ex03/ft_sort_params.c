#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while ((s1[i] == s2[i]) && (s1[i] != '\0' && s2[i] != '\0'))
	{
		i++;
	}
	return (s1[i] - s2[i]);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

void	ft_swap(char **a, char **b)
{
	char	*temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

int	main(int argc, char **argv)
{
	int	icnt;
	int	i;

	if (argc > 1)
	{
		icnt = 1;
		while (icnt < argc - 1)
		{
			if ((ft_strcmp(argv[icnt], argv[icnt + 1]) > 0))
			{
				ft_swap(&argv[icnt], &argv[icnt + 1]);
				icnt = 1;
			}
			else
				icnt++;
		}
		i = 1;
		while (i < argc)
		{
			ft_putstr(argv[i]);
			write(1, "\n", 1);
			i++;
		}
	}
	return (0);
}
