#include <unistd.h>

void ft_putstr(char *s1)
{
	int i;

	i = 0;

	while (s1[i] != '\0')
	{
		write(1, &s1[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int	main(void)
{
	ft_putstr("Hello World!");
	return (0);
}
